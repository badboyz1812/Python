#Print first ten even number using while loop
print("first 10 even numbers: ")
x = 1
while(x >= 1 and x <= 20):
    if(x%2 == 0):
        print(x)
        x += 1
    else:
        x += 1
        
print("first 10 odd number: ")
#print first ten odd number
x = 1
